package com.ibm.ws.jaxrs21.client.threadleak.server;

/**
 * Simplified data model representing Student data.
 *
 */
public class Student {
    private String id;
    private String status;

    public Student() {
    }

    public Student(String id, String status) {
        this.id = id;
        this.status = status;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    @Override
    public String toString() {
        return "Student{id='" + id + "', status='" + status + "'}";
    }
}

